    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String result = Client.register(jTextField3.getText(), Integer.parseInt(jTextField4.getText()), jTextField1.getText(), jTextField2.getText());
            jTextArea1.append(result + "\n");
            // parse code from "Registered: name Code: XXXX"
            if (result.contains("Code: ")) {
                myCode = result.split("Code: ")[1].trim();
            }
            // start peer listener on myPort (only once)
            if (!listening && !myCode.isEmpty()) {
                listening = true;
                int myPort = Integer.parseInt(jTextField2.getText());
                Thread t = new Thread(new Runnable() {
                    public void run() {
                        try {
                            java.net.ServerSocket ss = new java.net.ServerSocket(myPort);
                            log("Listening for peers on port " + myPort);
                            while (true) {
                                java.net.Socket peer = ss.accept();
                                new Thread(new PeerListener(peer, myCode)).start();
                            }
                        } catch (Exception e) { log("Listener error: " + e.getMessage()); }
                    }
                });
                t.start();
            }
        } catch (Exception e) { jTextArea1.append("Error: " + e.getMessage() + "\n"); }
    }//GEN-LAST:event_jButton1ActionPerformed

